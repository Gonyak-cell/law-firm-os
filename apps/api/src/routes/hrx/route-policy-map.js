function cleanPath(pathname) {
  return String(pathname ?? "").replace(/\/+$/, "") || "/";
}

function decodeParam(value) {
  return value ? decodeURIComponent(value) : undefined;
}

export function listHrxRoutePolicies() {
  return HRX_ROUTE_POLICIES.map((policy) => Object.freeze({ ...policy, pattern: policy.pattern.toString() }));
}

export function resolveHrxRoutePolicy({ method, pathname } = {}) {
  const normalizedMethod = String(method ?? "").toUpperCase();
  const normalizedPath = cleanPath(pathname);
  for (const policy of HRX_ROUTE_POLICIES) {
    if (policy.method !== normalizedMethod) continue;
    const match = normalizedPath.match(policy.pattern);
    if (!match) continue;
    const params = Object.freeze(
      Object.fromEntries(Object.entries(match.groups ?? {}).map(([key, value]) => [key, decodeParam(value)])),
    );
    const resourceId = policy.resource_param ? params[policy.resource_param] : normalizedPath;
    return Object.freeze({
      id: policy.id,
      method: policy.method,
      action: policy.action,
      sensitivity: policy.sensitivity,
      required_scope: policy.required_scope,
      purpose: policy.purpose,
      resource_type: policy.resource_type,
      resource_id: resourceId,
      params,
    });
  }
  return null;
}
