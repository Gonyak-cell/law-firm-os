export function dataRoomVdrCoreRowKey(title) {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}

export function dataRoomVdrCoreDeliverableType(title) {
  if (CONTRACT_TITLES.has(title)) return "contract";
  if (SECURITY_TITLES.has(title)) return "security_audit";
  if (HERMES_TITLES.has(title)) return "hermes_evidence";
  if (title === "Hermes model summary") return "hermes_evidence";
  if (CLAUDE_TITLES.has(title)) return "claude_review";
  if (title === "Claude model review prompt") return "claude_review";
  if (FIXTURE_TITLES.has(title)) return "fixture";
  if (TEST_TITLES.has(title)) return "test";
  if (UI_TITLES.has(title)) return "ui";
  if (FAILURE_RECOVERY_TITLES.has(title)) return "failure_recovery";
  return "implementation";
}

